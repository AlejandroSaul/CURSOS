import turtle

pantalla = turtle.Screen()
tortuga = turtle.Turtle()

pantalla.bgcolor("green")
pantalla.title("Tortuga")
tortuga.speed(1)
tortuga.shapesize(2,5,5)
tortuga.fillcolor("orange")
tortuga.color("blue","white")
tortuga.pensize(5)
tortuga.forward(100)
tortuga.pencolor("red")


turtle.done()